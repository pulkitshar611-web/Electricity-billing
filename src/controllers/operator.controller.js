const prisma = require('../config/prisma');

// ─────────────────────────────────────────
// GET /api/operator/dashboard
// Operator-specific dashboard stats
// ─────────────────────────────────────────
const getOperatorDashboard = async (req, res) => {
    try {
        const userId = req.user.id;

        // Get operator record
        const operator = await prisma.operator.findUnique({ where: { userId } });
        if (!operator) {
            return res.status(404).json({ success: false, message: 'Operator profile not found.' });
        }

        const operatorId = operator.id;
        const today = new Date();
        today.setHours(0, 0, 0, 0);
        const tomorrow = new Date(today);
        tomorrow.setDate(tomorrow.getDate() + 1);

        // Run all queries in parallel
        const [
            totalConsumers,
            billsGeneratedToday,
            pendingReadingsCount,
            todayCollections,
            recentBills,
            pendingComplaints,
        ] = await Promise.all([
            // Total consumers in system (operator serves all)
            prisma.consumer.count(),

            // Bills generated TODAY by this operator
            prisma.bill.count({
                where: {
                    operatorId,
                    createdAt: { gte: today, lt: tomorrow },
                },
            }),

            // Pending bills (no reading updated recently - bills with PENDING status)
            prisma.bill.count({
                where: { status: 'PENDING' },
            }),

            // Today's successful payments amount (for bills this operator generated)
            prisma.payment.aggregate({
                where: {
                    status: 'SUCCESS',
                    paidAt: { gte: today, lt: tomorrow },
                },
                _sum: { amount: true },
            }),

            // Recent bills generated by this operator
            prisma.bill.findMany({
                where: { operatorId },
                take: 5,
                orderBy: { createdAt: 'desc' },
                include: {
                    consumer: {
                        include: { user: { select: { name: true } } },
                    },
                },
            }),

            // Pending complaints for operator to handle
            prisma.complaint.findMany({
                where: { status: 'PENDING' },
                take: 5,
                orderBy: { createdAt: 'desc' },
                include: {
                    consumer: {
                        include: { user: { select: { name: true } } },
                    },
                },
            }),
        ]);

        res.status(200).json({
            success: true,
            data: {
                totalConsumers,
                billsGeneratedToday,
                pendingReadings: pendingReadingsCount,
                collectionsToday: Number(todayCollections._sum.amount || 0),
                recentBills: recentBills.map((b) => ({
                    id: b.id,
                    billNumber: b.billNumber,
                    consumerName: b.consumer.user.name,
                    amount: b.totalAmount,
                    status: b.status,
                    createdAt: b.createdAt,
                })),
                pendingComplaints: pendingComplaints.map((c) => ({
                    id: c.id,
                    consumerName: c.consumer.user.name,
                    subject: c.subject,
                    type: c.type,
                    createdAt: c.createdAt,
                })),
            },
        });
    } catch (error) {
        console.error('getOperatorDashboard Error:', error);
        res.status(500).json({ success: false, message: 'Server error.', error: error.message });
    }
};

// ─────────────────────────────────────────
// GET /api/operator/bills
// Bills generated by THIS operator
// ─────────────────────────────────────────
const getOperatorBills = async (req, res) => {
    try {
        const userId = req.user.id;
        const { status, page = 1, limit = 20 } = req.query;

        const operator = await prisma.operator.findUnique({ where: { userId } });
        if (!operator) return res.status(404).json({ success: false, message: 'Operator not found.' });

        const where = { operatorId: operator.id };
        if (status && status !== 'All') where.status = status.toUpperCase();

        const skip = (Number(page) - 1) * Number(limit);

        const [bills, total] = await Promise.all([
            prisma.bill.findMany({
                where,
                include: {
                    consumer: { include: { user: { select: { name: true } } } },
                },
                orderBy: { createdAt: 'desc' },
                skip,
                take: Number(limit),
            }),
            prisma.bill.count({ where }),
        ]);

        const formatted = bills.map((b) => ({
            id: b.id,
            billNumber: b.billNumber,
            consumerName: b.consumer.user.name,
            consumerId: b.consumerId,
            meterNumber: b.consumer.meterNumber,
            prevReading: b.prevReading,
            currReading: b.currReading,
            units: b.units,
            amount: b.totalAmount,
            dueDate: b.dueDate,
            billMonth: b.billMonth,
            status: b.status,
            createdAt: b.createdAt,
        }));

        res.status(200).json({
            success: true,
            count: formatted.length,
            total,
            page: Number(page),
            totalPages: Math.ceil(total / Number(limit)),
            data: formatted,
        });
    } catch (error) {
        console.error('getOperatorBills Error:', error);
        res.status(500).json({ success: false, message: 'Server error.' });
    }
};

// ─────────────────────────────────────────
// POST /api/operator/payments/receive
// Operator records a cash/offline payment
// ─────────────────────────────────────────
const receivePayment = async (req, res) => {
    try {
        const { billId, amount, mode } = req.body;

        if (!billId || !amount) {
            return res.status(400).json({ success: false, message: 'Bill ID and amount are required.' });
        }

        const bill = await prisma.bill.findUnique({
            where: { id: Number(billId) },
            include: { consumer: true },
        });

        if (!bill) return res.status(404).json({ success: false, message: 'Bill not found.' });
        if (bill.status === 'PAID') return res.status(400).json({ success: false, message: 'Bill is already paid.' });

        const payment = await prisma.$transaction(async (tx) => {
            const p = await tx.payment.create({
                data: {
                    billId: Number(billId),
                    consumerId: bill.consumerId,
                    amount: Number(amount),
                    mode: mode || 'CASH',
                    status: 'SUCCESS',
                    paidAt: new Date(),
                },
            });

            await tx.bill.update({
                where: { id: Number(billId) },
                data: { status: 'PAID' },
            });

            return p;
        });

        res.status(201).json({
            success: true,
            message: 'Payment recorded successfully.',
            data: {
                id: payment.id,
                transactionId: payment.transactionId,
                amount: payment.amount,
                mode: payment.mode,
                paidAt: payment.paidAt,
            },
        });
    } catch (error) {
        console.error('receivePayment Error:', error);
        res.status(500).json({ success: false, message: 'Server error.' });
    }
};

// ─────────────────────────────────────────
// GET /api/operator/payments
// All payments (operator view - recent collections)
// ─────────────────────────────────────────
const getOperatorPayments = async (req, res) => {
    try {
        const { page = 1, limit = 20, search } = req.query;
        const skip = (Number(page) - 1) * Number(limit);

        const where = {};
        if (search) {
            where.OR = [
                { consumer: { user: { name: { contains: search } } } },
                { bill: { billNumber: { contains: search } } },
            ];
        }

        const [payments, total] = await Promise.all([
            prisma.payment.findMany({
                where,
                include: {
                    consumer: { include: { user: { select: { name: true } } } },
                    bill: { select: { billNumber: true, billMonth: true } },
                },
                orderBy: { paidAt: 'desc' },
                skip,
                take: Number(limit),
            }),
            prisma.payment.count({ where }),
        ]);

        const formatted = payments.map((p) => ({
            id: p.id,
            transactionId: p.transactionId,
            consumerName: p.consumer.user.name,
            billNumber: p.bill.billNumber,
            billMonth: p.bill.billMonth,
            amount: p.amount,
            mode: p.mode,
            status: p.status,
            paidAt: p.paidAt,
        }));

        res.status(200).json({
            success: true,
            count: formatted.length,
            total,
            page: Number(page),
            totalPages: Math.ceil(total / Number(limit)),
            data: formatted,
        });
    } catch (error) {
        console.error('getOperatorPayments Error:', error);
        res.status(500).json({ success: false, message: 'Server error.' });
    }
};

// ─────────────────────────────────────────
// GET /api/operator/consumers
// All consumers (operator can view & search)
// ─────────────────────────────────────────
const getOperatorConsumers = async (req, res) => {
    try {
        const { search, status, type, page = 1, limit = 20 } = req.query;
        const skip = (Number(page) - 1) * Number(limit);

        const where = {};
        if (status && status !== 'All') where.status = status.toUpperCase();
        if (type && type !== 'All') where.connectionType = type.toUpperCase();
        if (search) {
            where.OR = [
                { user: { name: { contains: search } } },
                { meterNumber: { contains: search } },
                { address: { contains: search } },
            ];
        }

        const [consumers, total] = await Promise.all([
            prisma.consumer.findMany({
                where,
                include: {
                    user: { select: { id: true, name: true, email: true } },
                    _count: { select: { bills: true } },
                },
                orderBy: { createdAt: 'desc' },
                skip,
                take: Number(limit),
            }),
            prisma.consumer.count({ where }),
        ]);

        const formatted = consumers.map((c) => ({
            id: c.id,
            userId: c.userId,
            name: c.user.name,
            email: c.user.email,
            meterNumber: c.meterNumber,
            address: c.address,
            type: c.connectionType,
            status: c.status,
            lastReading: c.lastReading,
            billCount: c._count.bills,
            createdAt: c.createdAt,
        }));

        res.status(200).json({
            success: true,
            count: formatted.length,
            total,
            page: Number(page),
            totalPages: Math.ceil(total / Number(limit)),
            data: formatted,
        });
    } catch (error) {
        console.error('getOperatorConsumers Error:', error);
        res.status(500).json({ success: false, message: 'Server error.' });
    }
};

// ─────────────────────────────────────────
// GET /api/operator/consumers/:id
// Single consumer details (for bill generation)
// ─────────────────────────────────────────
const getOperatorConsumerById = async (req, res) => {
    try {
        const consumer = await prisma.consumer.findUnique({
            where: { id: Number(req.params.id) },
            include: {
                user: { select: { name: true, email: true } },
                bills: {
                    take: 5,
                    orderBy: { createdAt: 'desc' },
                    select: {
                        id: true,
                        billNumber: true,
                        totalAmount: true,
                        status: true,
                        billMonth: true,
                        createdAt: true,
                    },
                },
            },
        });

        if (!consumer) return res.status(404).json({ success: false, message: 'Consumer not found.' });

        res.status(200).json({
            success: true,
            data: {
                id: consumer.id,
                name: consumer.user.name,
                email: consumer.user.email,
                meterNumber: consumer.meterNumber,
                address: consumer.address,
                type: consumer.connectionType,
                status: consumer.status,
                lastReading: consumer.lastReading,
                recentBills: consumer.bills,
            },
        });
    } catch (error) {
        res.status(500).json({ success: false, message: 'Server error.' });
    }
};

